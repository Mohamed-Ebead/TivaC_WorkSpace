#ifndef STD_TYPES_H_
#define STD_TYPES_H_


typedef unsigned char        u_int_8     ;
typedef unsigned short int   u_int_16    ;
typedef unsigned long int    u_int_32    ;

typedef signed char          s_int_8     ;
typedef signed short int     s_int_16    ;
typedef signed long int      s_int_32    ;

typedef float                f_32       ;
typedef double               f_64       ;


#endif /* STD_TYPES_H_ */
